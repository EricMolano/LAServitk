const EditVehicle = ({ id, onClose }) => {
    const [vehicle, setVehicle] = useState({});
    const [modalMessage, setModalMessage] = useState('');

    useEffect(() => {
        const fetchVehicle = async () => {
            try {
                const response = await axios.get(`http://localhost:2071/api/vehicles/${id}`);
                setVehicle(response.data);
            } catch (error) {
                setModalMessage('Error al cargar los datos del vehículo.');
            }
        };
        fetchVehicle();
    }, [id]);

    const handleSubmit = async (e) => {
        e.preventDefault();
        try {
            await axios.put(`http://localhost:2071/api/vehicles/${id}`, vehicle);
            setModalMessage('Vehículo actualizado exitosamente.');
            onClose();
        } catch (error) {
            setModalMessage('Error al actualizar el vehículo.');
        }
    };

    return (
        <Modal onClose={onClose}>
            <h2>Editar Vehículo</h2>
            <form onSubmit={handleSubmit}>
                {/* Form inputs for updating marca, modelo, año, color, and placa */}
                <button type="submit">Actualizar Vehículo</button>
            </form>
        </Modal>
    );
};

export default EditVehicle;