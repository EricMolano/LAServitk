const EditVehicle = () => {
    const { id } = useParams();
    const [vehicle, setVehicle] = useState({ marca: '', modelo: '', año: '', color: '', placa: '' });
    const [error, setError] = useState('');
    const [models, setModels] = useState([]);
    const [years, setYears] = useState([]);
    const [modalOpen, setModalOpen] = useState(false); // Modal state
    const [modalMessage, setModalMessage] = useState(''); // Message for the modal
    const navigate = useNavigate();
    const token = localStorage.getItem('token');

    useEffect(() => {
        axios.get(`http://localhost:2071/api/vehicles/${id}`, {
            headers: {
                Authorization: `Bearer ${token}`
            }
        })
        .then(response => {
            console.log('Vehicle fetched successfully:', response.data);
            setVehicle(response.data);
            const { marca, modelo } = response.data;
            setModels(Object.keys(vehicleData[marca] || {}));
            setYears(vehicleData[marca][modelo] || []);
        })
        .catch(error => {
            console.error('Error fetching vehicle:', error);
            setModalMessage('Error al cargar el vehículo.');
            setModalOpen(true);
        });
    }, [id, token]);

    const handleChange = (e) => {
        const { name, value } = e.target;
        setVehicle(prevState => ({ ...prevState, [name]: value }));

        if (name === 'marca') {
            setModels(Object.keys(vehicleData[value] || {}));
            setYears(vehicleData[value][vehicle.modelo] || []);
        } else if (name === 'modelo') {
            setYears(vehicleData[vehicle.marca][value] || []);
        }
    };

    const handleSubmit = (e) => {
        e.preventDefault();

        axios.put(`http://localhost:2071/api/vehicles/${id}`, vehicle, {
            headers: {
                Authorization: `Bearer ${token}`
            }
        })
        .then(response => {
            console.log('Vehicle updated successfully:', response.data);
            setModalMessage('Vehículo actualizado exitosamente.');
            setModalOpen(true);
            // Optionally, you can navigate after a short delay
            setTimeout(() => {
                navigate('/ClientDashboard'); // Redirige al dashboard después de actualizar el vehículo
            }, 2000);
        })
        .catch(error => {
            console.error('Error updating vehicle:', error);
            setModalMessage('Error al actualizar el vehículo. Intenta nuevamente.');
            setModalOpen(true);
        });
    };

    const handleCloseModal = () => {
        setModalOpen(false);
    };

    return (
        <div className="edit-vehicle-container">
            <h1>Editar Vehículo</h1>
            {error && <p className="error-message">{error}</p>}
            <form onSubmit={handleSubmit}>
                <div className="form-group">
                    <label htmlFor="marca">Marca:</label>
                    <select
                        id="marca"
                        name="marca"
                        value={vehicle.marca}
                        onChange={handleChange}
                        required
                    >
                        <option value="">Selecciona una marca</option>
                        {Object.keys(vehicleData).map(brand => (
                            <option key={brand} value={brand}>
                                {brand}
                            </option>
                        ))}
                    </select>
                </div>
                <div className="form-group">
                    <label htmlFor="modelo">Modelo:</label>
                    <select
                        id="modelo"
                        name="modelo"
                        value={vehicle.modelo}
                        onChange={handleChange}
                        required
                    >
                        <option value="">Selecciona un modelo</option>
                        {models.map(model => (
                            <option key={model} value={model}>
                                {model}
                            </option>
                        ))}
                    </select>
                </div>
                <div className="form-group">
                    <label htmlFor="año">Año:</label>
                    <select
                        id="año"
                        name="año"
                        value={vehicle.año}
                        onChange={handleChange}
                        required
                    >
                        <option value="">Selecciona un año</option>
                        {years.map(year => (
                            <option key={year} value={year}>
                                {year}
                            </option>
                        ))}
                    </select>
                </div>
                <div className="form-group">
                    <label htmlFor="color">Color:</label>
                    <select
                        id="color"
                        name="color"
                        value={vehicle.color}
                        onChange={handleChange}
                    >
                        <option value="">Selecciona un color</option>
                        {colors.map(color => (
                            <option key={color} value={color}>
                                {color}
                            </option>
                        ))}
                    </select>
                </div>
                <button type="submit">Actualizar Vehículo</button>
            </form>
            <Modal isOpen={modalOpen} onClose={handleCloseModal} message={modalMessage} />
        </div>
    );
};

export default EditVehicle;