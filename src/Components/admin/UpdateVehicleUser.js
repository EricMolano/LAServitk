function UpdateVehicleUser() {
    const { idvehiculo } = useParams(); // Obtener el id del vehículo de la URL
    const navigate = useNavigate();
    const [vehicle, setVehicle] = useState({
        marca: '',
        modelo: '',
        año: '',
        color: '',
        placa: '',
    });
    const [models, setModels] = useState([]);
    const [years, setYears] = useState([]);
    const [loading, setLoading] = useState(true);
    const [error, setError] = useState(null);

    useEffect(() => {
        const fetchVehicle = async () => {
            try {
                const data = await getVehicles(idvehiculo); // Obtener los datos del vehículo
                setVehicle(data);
                
                // Cargar modelos y años del vehículo según la marca y modelo actual
                const { marca, modelo } = data;
                setModels(Object.keys(vehicleData[marca] || {}));
                setYears(vehicleData[marca]?.[modelo] || []);
            } catch (error) {
                setError('Error al cargar los datos del vehículo.');
            } finally {
                setLoading(false);
            }
        };

        fetchVehicle();
    }, [idvehiculo]);

    const handleChange = (e) => {
        const { name, value } = e.target;
        setVehicle({ ...vehicle, [name]: value });

        // Si cambia la marca, actualizamos los modelos
        if (name === 'marca') {
            setModels(Object.keys(vehicleData[value] || {}));
            setYears([]); // Limpiar los años al cambiar la marca
        }

        // Si cambia el modelo, actualizamos los años
        if (name === 'modelo') {
            setYears(vehicleData[vehicle.marca]?.[value] || []);
        }
    };

    const handleSubmit = async (e) => {
        e.preventDefault();
        try {
            await updateVehicleUser(idvehiculo, vehicle); // Actualiza los datos del vehículo
            navigate('/AdminDashboard'); // Redirige a la lista de vehículos después de actualizar
        } catch (error) {
            setError('Error al actualizar el vehículo.');
        }
    };

    if (loading) {
        return <div>Cargando datos del vehículo...</div>;
    }

    if (error) {
        return <div>{error}</div>;
    }

    return (
        <form onSubmit={handleSubmit}>
            <h2>Actualizar Vehículo</h2>
            <div className="form-group">
                <label htmlFor="marca">Marca:</label>
                <select
                    id="marca"
                    name="marca"
                    value={vehicle.marca}
                    onChange={handleChange}
                    required
                >
                    <option value="">Selecciona una marca</option>
                    {Object.keys(vehicleData).map(brand => (
                        <option key={brand} value={brand}>
                            {brand}
                        </option>
                    ))}
                </select>
            </div>
            <div className="form-group">
                <label htmlFor="modelo">Modelo:</label>
                <select
                    id="modelo"
                    name="modelo"
                    value={vehicle.modelo}
                    onChange={handleChange}
                    required
                >
                    <option value="">Selecciona un modelo</option>
                    {models.map(model => (
                        <option key={model} value={model}>
                            {model}
                        </option>
                    ))}
                </select>
            </div>
            <div className="form-group">
                <label htmlFor="año">Año:</label>
                <select
                    id="año"
                    name="año"
                    value={vehicle.año}
                    onChange={handleChange}
                    required
                >
                    <option value="">Selecciona un año</option>
                    {years.map(year => (
                        <option key={year} value={year}>
                            {year}
                        </option>
                    ))}
                </select>
            </div>
            <div className="form-group">
                <label htmlFor="color">Color:</label>
                <select
                    id="color"
                    name="color"
                    value={vehicle.color}
                    onChange={handleChange}
                >
                    <option value="">Selecciona un color</option>
                    {colors.map(color => (
                        <option key={color} value={color}>
                            {color}
                        </option>
                    ))}
                </select>
            </div>
          
            <button type="submit">Actualizar Vehículo</button>
        </form>
    );
}

export default UpdateVehicleUser;
